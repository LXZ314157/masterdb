public class LanguageText {
    private Integer no;
    private String zhsText;
    private String enText;

    public Integer getNo() {
        return no;
    }

    public void setNo(Integer no) {
        this.no = no;
    }

    public String getZhsText() {
        return zhsText;
    }

    public void setZhsText(String zhsText) {
        this.zhsText = zhsText;
    }

    public String getEnText() {
        return enText;
    }

    public void setEnText(String enText) {
        this.enText = enText;
    }
}
